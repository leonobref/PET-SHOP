   void preencherListaandSelect() throws IOException, BiffException{
       if(file.exists()){
          Workbook workbook = Workbook.getWorkbook(file);
        Sheet products = workbook.getSheet(2);
        int tam = products.getRows();
        String[] Produtos = new String[tam];
        for(int i = 1 ; i < Produtos.length; i++){
           Produtos[i] = products.getCell(0,i).getContents() ;
                    }
        jList1.setListData(Produtos); 
        int sele = 0;
        for(int j = 0; j <= Produtos.length; j++){
            if(Produtos[j].equals(novavenda.getProduct())){
                sele = j;
            }
        }
        jList1.setSelectedIndex(sele);
       }
       
       
   }

